#include <ios>
#include <iostream>
#include <vector>
#include <array>
#include <algorithm>

using namespace std;

void solve() {
	int n;
	cin >> n;
	vector<array<int, 2>> v(n);
	for (auto& [x, y] : v) cin >> x >> y;



}

int main() {
	ios_base::sync_with_stdio(false);
	cin.tie(nullptr);
	
	int t{1};
	cin >> t;
	while (t--) solve();
}
